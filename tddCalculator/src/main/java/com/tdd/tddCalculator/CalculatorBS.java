package com.tdd.tddCalculator;

public class CalculatorBS {
	public Integer multiply(Integer number1, Integer number2) {
		return number1 * number2;
	}
	
	public Integer divide(Integer dividendNumber1, Integer dividerNumber2) {
		return dividendNumber1 / dividerNumber2;
	}
	
	public Integer sum(Integer number1, Integer number2) {
		return number1 + number2;
	}
}
